import 'dart:ui';

import 'package:google_fonts/google_fonts.dart';



/// For Source poppins Google Font
myStyleSourcePoppins({required double fontSize, required Color color, FontWeight ?fontWeight, FontStyle? fontStyle,double? letterspacing }){
  return GoogleFonts.poppins(fontSize: fontSize, color: color, fontWeight: fontWeight, fontStyle: fontStyle,letterSpacing: letterspacing);
}


/// For Source Sans Pro Google Font
myStyleSourceHindSiliguri({required double fontSize, required Color color, FontWeight ?fontWeight, FontStyle? fontStyle,double? letterspacing }){
  return GoogleFonts.hindSiliguri(fontSize: fontSize, color: color, fontWeight: fontWeight, fontStyle: fontStyle,letterSpacing: letterspacing);
}



/// For Source Sans Pro Google Font
myStyleSourceInter({required double fontSize, required Color color, FontWeight ?fontWeight, FontStyle? fontStyle,double? letterspacing }){
  return GoogleFonts.inter(fontSize: fontSize, color: color, fontWeight: fontWeight, fontStyle: fontStyle,letterSpacing: letterspacing);
}
